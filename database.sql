CREATE TABLE `Division` (
  `DivisionID` int(11) NOT NULL AUTO_INCREMENT,
  `Name` varchar(50) NOT NULL,
  `AgeGroup` varchar(50) NOT NULL,
  `Gender` varchar(10) NOT NULL,
  PRIMARY KEY (`DivisionID`)
);

CREATE TABLE `Equipment` (
  `EquipmentID` int(11) NOT NULL AUTO_INCREMENT,
  `Name` varchar(100) NOT NULL,
  PRIMARY KEY (`EquipmentID`)
);

CREATE TABLE `Category` (
  `CategoryID` int(11) NOT NULL AUTO_INCREMENT,
  `DivisionID` int(11) NOT NULL,
  `EquipmentID` int(11) NOT NULL,
  `Name` varchar(50) NOT NULL,
  PRIMARY KEY (`CategoryID`),
  FOREIGN KEY (`DivisionID`) REFERENCES `Division`(`DivisionID`),
  FOREIGN KEY (`EquipmentID`) REFERENCES `Equipment`(`EquipmentID`)
);

CREATE TABLE `Competition` (
  `CompetitionID` int(11) NOT NULL AUTO_INCREMENT,
  `Name` varchar(100) NOT NULL,
  PRIMARY KEY (`CompetitionID`)
);

CREATE TABLE `Round` (
  `RoundID` int(11) NOT NULL AUTO_INCREMENT,
  `EquipmentID` int(11) NOT NULL,
  `CompetitionID` int(11) DEFAULT NULL,
  `Name` varchar(100) NOT NULL,
  `Distance` varchar(50) NOT NULL,
  `NumberOfEnds` int(11) NOT NULL,
  `TargetFace` varchar(50) NOT NULL,
  PRIMARY KEY (`RoundID`),
  FOREIGN KEY (`EquipmentID`) REFERENCES `Equipment`(`EquipmentID`),
  FOREIGN KEY (`CompetitionID`) REFERENCES `Competition`(`CompetitionID`)
);

CREATE TABLE `Archer` (
  `ArcherID` int(11) NOT NULL AUTO_INCREMENT,
  `CategoryID` int(11) NOT NULL,
  `FirstName` varchar(50) NOT NULL,
  `LastName` varchar(50) NOT NULL,
  `Gender` varchar(10) NOT NULL,
  `Age` int(11) NOT NULL,
  PRIMARY KEY (`ArcherID`),
  FOREIGN KEY (`CategoryID`) REFERENCES `Category`(`CategoryID`)
);

CREATE TABLE `Score` (
  `ScoreID` int(11) NOT NULL AUTO_INCREMENT,
  `RoundID` int(11) NOT NULL,
  `ArcherID` int(11) NOT NULL,
  `EquipmentID` int(11) NOT NULL,
  `Number` int(11) DEFAULT NULL,
  `Date` datetime NOT NULL,
  PRIMARY KEY (`ScoreID`),
  FOREIGN KEY (`RoundID`) REFERENCES `Round`(`RoundID`),
  FOREIGN KEY (`ArcherID`) REFERENCES `Archer`(`ArcherID`)
);

CREATE TABLE `ArrowScore` (
  `ArrowScoreID` int(11) NOT NULL AUTO_INCREMENT,
  `ScoreID` int(11) NOT NULL,
  `EndNumber` int(11) NOT NULL,
  `ArrowNumber` int(11) NOT NULL,
  `Number` int(11) NOT NULL,
  PRIMARY KEY (`ArrowScoreID`),
  FOREIGN KEY (`ScoreID`) REFERENCES `Score`(`ScoreID`)
);


-- Indexes

CREATE INDEX idx_archer_firstname ON Archer(FirstName);

CREATE INDEX idx_archer_lastname ON Archer(LastName);

CREATE INDEX idx_score_archerid ON Score(ArcherID);

CREATE INDEX idx_competitionid ON Competition(CompetitionID);

CREATE INDEX idx_roundid ON Round(RoundID);

CREATE INDEX idx_round_competitionid ON Round(CompetitionID);

CREATE INDEX idx_score_date ON Score(Date);

CREATE INDEX idx_score_roundid ON Score(RoundID);

